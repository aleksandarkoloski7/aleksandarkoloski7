#include<iostream>
using namespace std;
int main ()
{
    int godini,meseci,denovi,ostatokdenovi;
    cin>>denovi;
    godini=denovi/365;
    meseci = (denovi%365)/30;
    ostatokdenovi = denovi%365%30;
    cout<<"Years: "<<godini<<","<<" months: "<<meseci<<","<<" days: "<<ostatokdenovi;
    return 0;
}
